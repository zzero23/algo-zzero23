public class Main {
    public static void main(String[] args) {
        // 변수 정의
        int a = 5;
        int b = 30;
        // 연산
        int c = a + b; // 35

        double quot = c / 8; // 4
        int remain = c % 8; // 3

        // 출력
        System.out.println((int)quot);
        System.out.println(remain);
    }
}
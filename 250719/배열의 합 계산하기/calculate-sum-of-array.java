public class Main {
    public static void main(String[] args) {
        // 배열 선언
        int[] arrayA = {3, 1, 4, 5, 9};

        // 인덱스 값 추출
        int num1 = arrayA[1];
        int num3 = arrayA[3];
        int num4 = arrayA[4];

        // 합 출력
        System.out.println(num1 + num3 + num4);
    }
}